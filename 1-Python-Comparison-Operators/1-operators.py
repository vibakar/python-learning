# Equal
print(2 == 2)
print(1 == 0)

# Note that == is a comparison operator, while = is an assignment operator.
# Not Equal
print(2 != 1)
print(2 != 2)

# Greater Than
print(2 > 1)
print(2 > 4)

# Less Than
print(2 < 4)
print(2 < 1)

# Greater Than or Equal to
print(2 >= 2)
print(2 >= 1)

# Less than or Equal to
print(2 <= 2)
print(2 <= 4)

